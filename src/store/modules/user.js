import { getToken, setToken, removeToken } from '@/utils/auth'
import { login } from '@/api/user'
const state = {
  // 设置token初始状态
  token: getToken()
}
const mutations = {
  // 设置 token
  setToken(state, token) {
    // 改变vuex
    state.token = token
    // 改变本地缓存
    setToken(token)
  },
  // 删除缓存
  removeToken(state) {
    // 改变vuex
    state.token = null
    // 改变本地缓存
    removeToken()
  }
}
const actions = {
  async login(context, data) {
    const result = await login(data)
    context.commit('setToken', result)
  }
}
export default {
  namespaced: true,
  state,
  mutations,
  actions
}
